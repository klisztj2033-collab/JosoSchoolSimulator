/* 会話直前の文字列から、末尾に最も近い人物名トークンを返す */
function detectSpeaker(pre) {
  let best = null, bestIdx = -1;
  for (const s of SPEAKER_TOKENS) {
    const idx = pre.lastIndexOf(s.t);
    if (idx > bestIdx) { bestIdx = idx; best = s; }
  }
  if (!best) return null;
  // display名に助詞・助動詞が末尾に混入しても除去する保険
  const display = best.d.replace(/[がはをにへもでとよりからついて]$/, "");
  return { display, idx: bestIdx, token: best.t };
}

