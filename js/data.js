/* 一般生徒の表示名（Excel列式: A,B,…,Z,AA,AB,… で94人ぶん命名） */
function genericLabel(n) {
  let s = "";
  n += 1;
  while (n > 0) {
    const m = (n - 1) % 26;
    s = String.fromCharCode(65 + m) + s;
    n = Math.floor((n - 1) / 26);
  }
  return "一般生徒" + s;
}

